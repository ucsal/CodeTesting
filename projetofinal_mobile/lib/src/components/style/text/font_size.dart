///O [FontSize] é uma classe responsável por gerenciar e armazenar variádos
///tipos de fontes de textos.
///**Exemplo:**
/// ```dart
/// fontSize: FontSize.xxxLarge,
/// ```
class FontSize {
  ///xxSmall = 10
  static const double xxSmall = 10.0;

  ///xSmall = 12.0
  static const double xSmall = 12.0;

  ///small = 14.0
  static const double small = 14.0;

  ///medium = 16.0
  static const double medium = 16.0;

  ///large = 18.0
  static const double large = 18.0;

  ///xLarge = 20.0
  static const double xLarge = 20.0;

  ///xxLarge = 22.0
  static const double xxLarge = 22.0;

  ///xxxLarge = 24.0
  static const double xxxLarge = 24.0;

  ///xxxxLarge = 28.0
  static const double xxxxLarge = 28.0;
}
